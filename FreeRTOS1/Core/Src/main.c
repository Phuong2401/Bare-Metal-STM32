void LedRedControllerTask( void* PVParemetter)
{
	while(1)
	{
//		HAL_GPIO_TogglePin(GPIOD, (uint16_t)PVParemetter);
		RedCount ++;
		for(int i = 0; i < 100000;i++){}

		Suspend_Monitor++;

	   if(Suspend_Monitor >= 100)
	   {
		  is_suspended = TRUE;
		  Suspend_Monitor = 0;
		  vTaskSuspend(NULL);
	   }
	}

}
void LedOrangeControllerTask( void* PVParemetter)
{
	while(1)
	{
//		HAL_GPIO_TogglePin(GPIOD, (uint16_t)PVParemetter);
		BlueCount ++;
		for(int i = 0; i < 100000;i++){}

		if(is_suspended == TRUE)
		{
			Resume_Monitor++;
			if(Resume_Monitor >= 200)
			{
				vTaskResume(Red_Handle);
				Resume_Monitor = 0;
				is_suspended = FALSE;
			}
		}
	}

}


